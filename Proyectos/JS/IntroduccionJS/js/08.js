// Contatenación y Template Strings

// Concatenación usando el operador +
let nombre = "Jorge";
let saludo = "Hola, " + nombre + " Bienvenido";
console.log(saludo);

// Concatenación usando template strings ``
let usuario = "Usuario1";
let email = "usuario1@correo.com"

let mensaje = `Hola ${usuario}, tu correo es ${email}`;
console.log(mensaje);